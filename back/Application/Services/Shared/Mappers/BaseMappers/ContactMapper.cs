using Application.Services.Shared.Dtos;
using Domain.Entities.Shared;

namespace Application.Services.Shared.Mappers.BaseMappers;

public class ContactEntityMapper : BaseMapper<Contact, ContactDto>
{
    public override ContactDto Map(Contact source)
    {
        if (source == null) return new ContactDto();

        var destination = base.Map(source);

        return destination;
    }
}
public class ContactDtoMapper : BaseMapper<ContactDto, Contact>
{
    public override ContactDto Map(Contact source)
    {
        if (source == null) return new ContactDto();

        var destination = base.Map(source);

        return destination;
    }
}
